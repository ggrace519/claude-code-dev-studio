#!/usr/bin/env python3
"""
build-marketplace.py — generates the native Claude Code plugin marketplace
from the library source tree.

Maps the playbook's pack architecture 1:1 onto plugins:

  - ccds-core         the 5 core agents + the always-on cross-cutting skills
                      (playbook-conventions, api-design, ux-design,
                      security-checklist, code-review-checklist, common-*)
  - ccds-<pack> x14   the pack's domain agent + its <pack>-* skills

Output (checked in, regenerated by this script — CI gates freshness):

  .claude-plugin/marketplace.json     the marketplace catalog
  plugins/ccds-<name>/
    .claude-plugin/plugin.json
    agents/<agent>.md                 copied from .claude/agents/
    skills/<skill>/SKILL.md           copied from skills/

Users then install with:
  /plugin marketplace add ggrace519/claude-code-dev-studio
  /plugin install ccds-saas@ccds

Generation is deterministic (sorted, no timestamps, no git state) so CI can
regenerate and `git diff --exit-code` to detect drift. Plugins are emitted
without a `version` field by default: a git-hosted marketplace lets the commit
SHA drive updates, which keeps the committed tree byte-stable across release
tags (the freshness check never races a version bump). Pass --version to pin an
explicit semver into every plugin. The `sync-agents` skill is intentionally
excluded from plugins: it drives the ZIP-install JIT staging flow, which plugin
enablement replaces.

Usage:
    python3 scripts/build-marketplace.py [repo-root] [--version X.Y.Z]
"""

import json
import os
import re
import shutil
import sys

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
REPO_ROOT = os.path.abspath(sys.argv[1] if len(sys.argv) > 1 and not sys.argv[1].startswith("--")
                            else os.path.dirname(SCRIPT_DIR))

AGENTS_DIR = os.path.join(REPO_ROOT, ".claude", "agents")
SKILLS_DIR = os.path.join(REPO_ROOT, "skills")
PLUGINS_DIR = os.path.join(REPO_ROOT, "plugins")
MARKETPLACE_DIR = os.path.join(REPO_ROOT, ".claude-plugin")

CORE_AGENTS = ["plan-architect", "pr-code-reviewer", "secure-auditor",
               "test-writer-runner", "deploy-checklist"]
CORE_SKILLS = ["api-design", "code-review-checklist", "playbook-conventions",
               "security-checklist", "ux-design"]
PACKS = ["ai", "dataplat", "desktop", "devtool", "ecom", "embed", "ext",
         "fintech", "game", "infra", "media", "mobile", "orch", "saas"]

OWNER = {"name": "Greg Grace", "email": "contact@519lab.com"}
REPO_URL = "https://github.com/ggrace519/claude-code-dev-studio"


def version_arg():
    """Explicit --version pins every plugin to that string. Omitted by default:
    a git-hosted marketplace lets the commit SHA drive updates (every commit is
    a new version), so the committed tree stays byte-stable across release tags
    and the marketplace-freshness CI check never races the version bump."""
    for i, a in enumerate(sys.argv):
        if a == "--version" and i + 1 < len(sys.argv):
            return sys.argv[i + 1]
    return None


def frontmatter_field(path, name):
    content = open(path, encoding="utf-8").read()
    m = re.match(r'^---\s*\n(.*?)\n---', content, re.DOTALL)
    if not m:
        return ""
    f = re.search(rf'^{name}:\s*(.+)$', m.group(1), re.MULTILINE)
    return f.group(1).strip().strip('"').strip("'") if f else ""


def write_json(path, obj):
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8", newline="\n") as f:
        json.dump(obj, f, indent=2, ensure_ascii=False)
        f.write("\n")


def copy_agent(name, plugin_dir):
    src = os.path.join(AGENTS_DIR, f"{name}.md")
    dst = os.path.join(plugin_dir, "agents", f"{name}.md")
    os.makedirs(os.path.dirname(dst), exist_ok=True)
    shutil.copyfile(src, dst)


def copy_skill(name, plugin_dir):
    src = os.path.join(SKILLS_DIR, name)
    dst = os.path.join(plugin_dir, "skills", name)
    shutil.copytree(src, dst)


def build_plugin(name, description, agents, skill_names, version):
    plugin_dir = os.path.join(PLUGINS_DIR, name)
    manifest = {
        "name": name,
        "description": description,
        "author": OWNER,
        "homepage": REPO_URL,
        "license": "PolyForm-Noncommercial-1.0.0",
    }
    if version:
        manifest["version"] = version
    write_json(os.path.join(plugin_dir, ".claude-plugin", "plugin.json"), manifest)
    for a in agents:
        copy_agent(a, plugin_dir)
    for s in skill_names:
        copy_skill(s, plugin_dir)
    entry = {
        "name": name,
        # Explicit relative path rather than pluginRoot + bare name: identical
        # meaning, but supported by older Claude Code versions too.
        "source": f"./plugins/{name}",
        "description": description,
        "category": "workflow" if name == "ccds-core" else "domain-pack",
        "tags": ["ccds", "playbook"] + ([] if name == "ccds-core" else [name.removeprefix("ccds-")]),
    }
    if version:
        entry["version"] = version
    return entry


def main():
    version = version_arg()
    for d in (AGENTS_DIR, SKILLS_DIR):
        if not os.path.isdir(d):
            print(f"ERROR: not found: {d}", file=sys.stderr)
            return 2

    # Full regeneration: the script owns these trees outright.
    shutil.rmtree(PLUGINS_DIR, ignore_errors=True)
    os.makedirs(PLUGINS_DIR)

    all_skills = sorted(d for d in os.listdir(SKILLS_DIR)
                        if os.path.isfile(os.path.join(SKILLS_DIR, d, "SKILL.md")))
    common_skills = [s for s in all_skills if s.startswith("common-")]

    entries = [build_plugin(
        "ccds-core",
        "Claude Code Dev Studio core: 5 generalist agents (plan, review, security, "
        "test, deploy) plus the cross-cutting skills every project uses.",
        CORE_AGENTS, sorted(CORE_SKILLS + common_skills), version)]

    for pack in PACKS:
        agent = f"{pack}-architect"
        pack_skills = [s for s in all_skills if s.startswith(f"{pack}-")]
        desc = frontmatter_field(os.path.join(AGENTS_DIR, f"{agent}.md"), "description")
        first_sentence = desc.split(". ")[0].rstrip(".") + "."
        entries.append(build_plugin(
            f"ccds-{pack}",
            f"{first_sentence} Ships the {agent} agent and {len(pack_skills)} "
            f"{pack}-* skills it composes.",
            [agent], pack_skills, version))

    metadata = {
        "description": "Claude Code Dev Studio — domain-agent + skills playbook "
                       "as native plugins. One plugin per archetype pack.",
    }
    if version:
        metadata["version"] = version
    marketplace = {
        "$schema": "https://anthropic.com/claude-code/marketplace.schema.json",
        "name": "ccds",
        "owner": OWNER,
        "metadata": metadata,
        "plugins": entries,
    }
    write_json(os.path.join(MARKETPLACE_DIR, "marketplace.json"), marketplace)

    # Self-check: every listed source resolves, has a manifest and components.
    failures = 0
    for e in entries:
        pdir = os.path.join(PLUGINS_DIR, e["name"])
        if not os.path.isfile(os.path.join(pdir, ".claude-plugin", "plugin.json")):
            print(f"ERROR: {e['name']}: missing plugin.json", file=sys.stderr)
            failures += 1
        if not (os.path.isdir(os.path.join(pdir, "agents")) or os.path.isdir(os.path.join(pdir, "skills"))):
            print(f"ERROR: {e['name']}: no components", file=sys.stderr)
            failures += 1
    if failures:
        return 1

    n_agents = sum(len(os.listdir(os.path.join(PLUGINS_DIR, e["name"], "agents")))
                   for e in entries if os.path.isdir(os.path.join(PLUGINS_DIR, e["name"], "agents")))
    n_skills = sum(len(os.listdir(os.path.join(PLUGINS_DIR, e["name"], "skills")))
                   for e in entries if os.path.isdir(os.path.join(PLUGINS_DIR, e["name"], "skills")))
    print(f"marketplace.json -> {os.path.join(MARKETPLACE_DIR, 'marketplace.json')}")
    stamp = f"version {version}" if version else "unversioned (git-commit-driven)"
    print(f"{len(entries)} plugins ({n_agents} agents + {n_skills} skills), {stamp}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
